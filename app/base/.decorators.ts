      import 'reflect-metadata';
      import { PageLevel }          from "./.interfaces.js";
      
      /*||=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-||
      //|| Endpoint Decorator
      //||=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-=-==-=-=-=-=-=-=-=-=-=-=-=-=--=-=-=-||*/
      
      export function endPoint(path: string, method: "GET" | "POST", level: PageLevel = 100) {
          return (target: any) => {
              Reflect.defineMetadata('route', { path, method, securityLevel: level, type: 'endpoint' }, target);
          };
      }

